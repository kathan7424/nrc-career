<script>
        (function() {
        'use strict';
        
        document.addEventListener('DOMContentLoaded', function() {
            const faqItems = document.querySelectorAll('.multicol-faqs-item');
            
            if (!faqItems.length) {
                console.warn('No FAQ items found');
                return;
            }
            
            // Function to close all FAQ items
            function closeAllFAQs() {
                faqItems.forEach(item => {
                    item.classList.remove('active');
                    const question = item.querySelector('.multicol-faqs-question');
                    if (question) {
                        question.setAttribute('aria-expanded', 'false');
                    }
                });
            }
            
            // Function to open a specific FAQ item
            function openFAQ(item) {
                item.classList.add('active');
                const question = item.querySelector('.multicol-faqs-question');
                if (question) {
                    question.setAttribute('aria-expanded', 'true');
                }
            }
            
            // Function to toggle FAQ item
            function toggleFAQ(item) {
                const isActive = item.classList.contains('active');
                
                if (isActive) {
                    // Close this item
                    item.classList.remove('active');
                    const question = item.querySelector('.multicol-faqs-question');
                    if (question) {
                        question.setAttribute('aria-expanded', 'false');
                    }
                } else {
                    // Close all items first (optional - remove if you want multiple open)
                    // closeAllFAQs();
                    
                    // Open this item
                    openFAQ(item);
                }
            }
            
            // Add click event listeners to all FAQ questions
            faqItems.forEach(item => {
                const question = item.querySelector('.multicol-faqs-question');
                
                if (question) {
                    question.addEventListener('click', function(e) {
                        e.preventDefault();
                        toggleFAQ(item);
                    });
                    
                    // Keyboard accessibility
                    question.addEventListener('keydown', function(e) {
                        if (e.key === 'Enter' || e.key === ' ') {
                            e.preventDefault();
                            toggleFAQ(item);
                        }
                    });
                }
            });
            
            // Initialize: Set proper aria-expanded values
            faqItems.forEach(item => {
                const question = item.querySelector('.multicol-faqs-question');
                if (question) {
                    const isActive = item.classList.contains('active');
                    question.setAttribute('aria-expanded', isActive ? 'true' : 'false');
                }
            });
            
            console.log('FAQ accordion initialized with', faqItems.length, 'items');
        });
    })();
</script>